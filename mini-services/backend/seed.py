def seed_database():
    """Create tables and seed vocabulary data.

    If the database already has data, this will add any NEW words
    that don't already exist (by english+vietnamese pair).
    """
    # Create all tables
    Base.metadata.create_all(bind=engine)

    db = SessionLocal()
    try:
        existing_count = db.query(Vocabulary).count()

        if existing_count == 0:
            # Fresh database - insert all at once
            for item in VOCABULARY_DATA:
                vocab = Vocabulary(**item)
                db.add(vocab)

            db.commit()
            print(f"[seed] Successfully seeded {len(VOCABULARY_DATA)} vocabulary items.")
        else:
            # Existing database - add only new words
            existing_pairs = set(
                (v.english, v.vietnamese)
                for v in db.query(Vocabulary.english, Vocabulary.vietnamese).all()
            )

            new_items = [
                item for item in VOCABULARY_DATA
                if (item["english"], item["vietnamese"]) not in existing_pairs
            ]

            if new_items:
                for item in new_items:
                    vocab = Vocabulary(**item)
                    db.add(vocab)

                db.commit()
                print(f"[seed] Added {len(new_items)} new vocabulary items (had {existing_count}).")
            else:
                print(f"[seed] Database already has {existing_count} vocabulary items. No new words to add.")

        # Print category summary
        total = db.query(Vocabulary).count()
        categories = {}
        for item in VOCABULARY_DATA:
            cat = item.get("category", "Uncategorized")
            categories[cat] = categories.get(cat, 0) + 1

        print(f"[seed] Total in DB: {total} words across {len(categories)} categories")

        # ── Seed admin user ──────────────────────────────────────
        from models import User
        from auth import hash_password

        admin_email = "galaxyvn@learnhub.com"
        # Ensure we have a clean admin user
        existing_admin = db.query(User).filter(User.email == admin_email).first()
        if existing_admin:
            db.delete(existing_admin)
            db.commit()

        admin_user = User(
            id="u_galaxyvn_admin",
            email=admin_email,
            name="galaxyvn",
            password=hash_password("Abc@123"),
            role="admin",
            xp_points=0,
            current_level=1,
            last_reviewed_at=datetime.now(timezone.utc),
        )
        db.add(admin_user)
        db.commit()
        print(f"[seed] Admin user created: {admin_email} / Abc@123")

        # ── Seed test gamification user ─────────────────────────────────────
        test_email = "gamer@learnhub.com"
        # Ensure we have a clean test user
        existing_test_user = db.query(User).filter(User.email == test_email).first()
        if existing_test_user:
            db.delete(existing_test_user)
            db.commit()

        test_user = User(
            id="u_gamer_test",
            email=test_email,
            name="Gamer Test",
            password=hash_password("Abc123"),
            xp_points=3800,
            current_level=8,
            last_reviewed_at=datetime.now(timezone.utc),
        )
        db.add(test_user)

        # Add 3 badges for test user
        test_badge_codes = [FIRST_BLOOD, STREAK_3, MASTER_10]
        for badge_code in test_badge_codes:
            badge = UserBadge(
                user_id=test_user.id,
                badge_code=badge_code,
                unlocked_at=datetime.now(timezone.utc)
            )
            db.add(badge)

        # Seed a beautiful, highly realistic, and database-honest review log history for the test user gamer@learnhub.com
        # Spanning the last 90 days, producing ~60 active study days with 5-15 reviews each.
        import random
        # Seed the random number generator deterministically for consistent seeding
        random.seed(12345)
        
        vocabularies = db.query(Vocabulary).all()
        vocab_count = len(vocabularies)
        
        if vocab_count > 0:
            now = datetime.now(timezone.utc)
            total_days_to_seed = 90
            logs_added = 0
            
            for day_offset in range(total_days_to_seed, -1, -1):
                # Study on ~65% of the days
                # But guarantee study on yesterday and today to preserve due cards / today's active streak
                is_yesterday_or_today = day_offset <= 1
                if not is_yesterday_or_today and random.random() > 0.65:
                    continue
                
                # Number of reviews on this day
                num_reviews = random.randint(5, 15) if not is_yesterday_or_today else random.randint(8, 12)
                study_date = now - timedelta(days=day_offset)
                
                # Track reviewed words on this day to prevent duplicate logs of the same word in the same day
                reviewed_vocab_ids = set()
                
                for _ in range(num_reviews):
                    vocab = random.choice(vocabularies)
                    if vocab.id in reviewed_vocab_ids:
                        continue
                    reviewed_vocab_ids.add(vocab.id)
                    
                    # Randomize time of study on that day (daytime hours: 8:00 to 22:00)
                    hour = random.randint(8, 22)
                    minute = random.randint(0, 59)
                    second = random.randint(0, 59)
                    log_time = study_date.replace(hour=hour, minute=minute, second=second)
                    
                    rating = random.choice([1, 2, 3, 4, 3, 4])  # Weighted towards Good (3) and Easy (4)
                    ease_factor = round(random.uniform(1.8, 3.0), 2)
                    interval_days = random.choice([1, 3, 7, 14, 30])
                    repetitions = random.randint(1, 5)
                    response_time = random.randint(1000, 8000)
                    
                    review_log = ReviewLog(
                        user_id=test_user.id,
                        vocabulary_id=vocab.id,
                        rating=rating,
                        ease_factor=ease_factor,
                        interval_days=interval_days,
                        repetitions=repetitions,
                        reviewed_at=log_time,
                        next_review_at=log_time + timedelta(days=interval_days),
                        response_time_ms=response_time,
                        direction=random.choice(["en_to_vi", "vi_to_en"])
                    )
                    db.add(review_log)
                    logs_added += 1
            
            print(f"[seed] Seeded {logs_added} realistic review logs for {test_user.email} across {total_days_to_seed} days.")

        db.commit()
        print(f"[seed] Test user created: {test_email} / Abc@123 / Level {test_user.current_level} ({test_user.xp_points} XP, 3 badges)")
        print(f"[seed] Seeded review history for demo: 5 due cards, 3 mastered cards")

    except Exception as e:
        print(f"[seed] Error seeding database: {e}")
        db.rollback()
        raise
    finally:
        db.close()


if __name__ == "__main__":
    seed_database()
